export function makeT(locale) {
  const d = ui[locale] || ui.en;
  return (key) => d[key] ?? ui.en[key] ?? key;
}

export function localize(base, locale) {
  if (locale === "en") return base;
  const m = maps[locale] || maps.en;
  const tg = taglines[locale] || taglines.en;
  const deg = degrees[locale] || degrees.en;
  const ps = projectSummaries[locale] || projectSummaries.en;
  const eb = expBullets[locale];
  const hk = hackathons[locale];
  const ls = lists[locale];

  return {
    ...base,
    tagline: tg.tagline,
    currently: tg.currently,
    experience: base.experience.map(e => ({
      ...e,
      role: m.roles[e.role] ?? e.role,
      type: m.types[e.type] ?? e.type,
      bullets: eb?.[e.id] ?? e.bullets,
    })),
    projects: base.projects.map(p => ({
      ...p,
      summary: ps[p.id] ?? p.summary,
    })),
    moreProjects: (base.moreProjects || []).map(p => ({
      ...p,
      summary: ps[p.id] ?? p.summary,
    })),
    achievements: {
      ...base.achievements,
      hackathons: base.achievements.hackathons.map((a, i) => ({
        ...a,
        title: hk?.[i]?.title ?? a.title,
        detail: hk?.[i]?.detail ?? a.detail,
      })),
    },
    education: base.education.map((e, i) => ({
      ...e,
      degree: deg[i] ?? e.degree,
    })),
    skills: Object.fromEntries(
      Object.entries(base.skills).map(([group, items]) => [
        m.skillGroups[group] ?? group,
        items,
      ])
    ),
  };
}
